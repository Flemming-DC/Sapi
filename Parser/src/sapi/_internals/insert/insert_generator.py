def insert_into_tree(cur: Cursor, json: dict[list[dict]]):
    assert len(json.items()) == 1, f"Expected only one tree or subtree to insert into, but received {list(json.keys())}"
    root = list(json.keys())[0]
    data = list(json.values())[0]
    _recursive_insert_into_tree(cur, root, data, [], None)
    # hyp return the ids, or perhaps just the top level id?


def _recursive_insert_into_tree(cur: Cursor, tab_name: str, data: list[dict], parent_ids: list[int], parent_id_name: str):
    if data == []: 
        return # evt. return query, args
    assert bool(parent_id_name) == bool(parent_ids), ""
    # check that tab_name is alphanumeric

    id_name = tab_name + '_id' # handle user defined name of id
    query, args = _generate_immediate_table_insert(tab_name, data, parent_ids, parent_id_name)

    # execute
    # print(_put_args_into_query(query, args))
    cur.execute(query, args)
    ids: list[int] = [id_row[0] for id_row in cur.fetchall()]
    assert len(ids) == len(data), ""

    children_data = _from_hieracical_to_sql_format(data)
    # recursion
    for child in children_data:
        child_data = children_data[child]
        _recursive_insert_into_tree(cur, child, child_data, ids, id_name)

def _generate_immediate_table_insert(tab_name: str, data: list[dict], parent_ids: list[int], parent_id_name: str):
    args = []
    query = "" # hyp: find a suitable stream type for performance

    # build query and args
    first_row = dict(data[0])
    if parent_ids:
        first_row[parent_id_name] = parent_ids[0]
        
    query += f"insert into {tab_name} (" 
    query += ', '.join([col for col, datum in first_row.items() if not _is_subtree(datum)])
    query += ") values\n"

    arg_count = len([d for d in first_row.values() if not _is_subtree(d)]) 
    piece = '(' + ('%s, ' * arg_count).rstrip(', ') + ')'
    row_count = len(data)
    for i, row in enumerate(data):
        query += piece + (',\n' if i == row_count else '\n')
        # line assumes that the foreign keys are not given in advance, but must be autogenerated.
        args += [d for d in row.values() if not _is_subtree(d)]
        if parent_ids:
            args.append(parent_ids[i])
    id_name = tab_name + '_id' # handle user defined name of id
    # optionally quit the returning clause for the leaves
    query += f"returning {id_name};\n" # non-postgress databases will need something different here.

    return query, args


