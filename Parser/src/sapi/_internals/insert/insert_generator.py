# ------------- generate insert ------------- #
_Pickup = list[tuple[str, list]] # list of (query, args)

def generate_insert(json: dict[list[dict]]) -> tuple[str, list]:
    assert len(json.items()) == 1, f"Expected only one tree or subtree to insert into, but received {list(json.keys())}"
    root = list(json.keys())[0]
    data = list(json.values())[0]
    # if isinstance(data, dict):
    #     data = [data]
    pickup = _recursive_generate_insert([], root, data, None)
    query = ';\n'.join(p[0] for p in pickup)
    args = []
    for p in pickup:
        args += p[1]
    return query, args

def _recursive_generate_insert(pickup: _Pickup, tab_name: str, data: list[dict], parent: str) -> _Pickup:
    assert pickup is not None, ""
    if data == []: return pickup
    # assert tab_name.isascii() and tab_name.replace('_', '').isalnum(), "tab_name should be ascii and alhanumeric"

    query, args = _generate_table_insert(tab_name, data, parent)
    pickup.append((query, args))
    children_data = _from_hieracical_to_sql_format(data)
    for child_name, child_data in children_data.items():
        pickup = _recursive_generate_insert(pickup, child_name, child_data, tab_name)
    return pickup


def _generate_table_insert(tab_name: str, data: list[dict], parent: str) -> tuple[str, list]:
    args = []
    query = "" # hyp: find a suitable stream type for performance

    # build query and args
    columns = [col for col, datum in data[0].items() if not _is_subtree(datum)]
    if parent:
        parent_id_name = parent + '_id'
        columns = [parent_id_name] + columns
        query += f"with parent as (select max({parent_id_name}) as id from {parent})\n"
    query += f"insert into {tab_name} (" 
    query += ', '.join(columns)
    query += ")\n"

    arg_count = len([d for d in data[0].values() if not _is_subtree(d)]) 
    piece = 'select ' 
    if parent: piece += 'parent.id, '
    piece += '%s, ' * arg_count
    piece = piece.rstrip(', ')
    if parent: piece += ' from parent '

    last_index = len(data) - 1
    for i, row in enumerate(data):
        query += piece + ('union\n' if i < last_index else '\n')
        args += [d for d in row.values() if not _is_subtree(d)]

    return query, args


